day_of_week = int(input("Введите порядковый номер текущего дня недели: "))
if day_of_week == 1:
    print("Понедельник")
elif day_of_week == 2:
    print("Вторник")
elif day_of_week == 3:
    print("Среда")
elif day_of_week == 4:
    print("Четверг")
elif day_of_week == 5:
    print("Пятница")
elif day_of_week == 6:
    print("Суббота")
elif day_of_week == 7:
    print("Воскресенье")
else:
    print("Такого номера дня недели не существует")
